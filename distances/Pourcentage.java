package distances;

public class Pourcentage {

	public static double calRePrcen(double nbrMs,double degree)
	{
		double nbr;
		nbr=(degree*nbrMs)/100;
	
		return nbr;
		
	}
	public static double calPrcendt(double nbrMs,double nbr)
	{
		double p;
		p=(nbr*100)/nbrMs;
	
		return p;
		
	}
}
